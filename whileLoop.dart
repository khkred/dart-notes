void main() {

    //Print 1 to 5 using a while loop

    //You have to a give a initial value to the number eg: a = 1
    var a = 1;

    //While loop only has the condition. Which repeats until false
    while(a<=5) {

        //Execution body
        print(a);

        //In While loop you also need to write increment;
        a++; // also can be written as a++;
    }
}